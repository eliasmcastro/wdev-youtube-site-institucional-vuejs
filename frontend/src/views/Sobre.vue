<template>
  <main>
    <section class="container">
      <h1>Sobre</h1>
      <div class="about" v-html="about"></div>
    </section>
  </main>
</template>

<script>
import api from '@/services/api.js';

export default {
  name: 'Sobre',

  data() {
    return {
      about: '',
    };
  },

  mounted() {
    api.get('/about').then((res) => {
      this.about = res.data.about;
    });
  },
}
</script>

<style scoped>
main {
  align-items: center;
}

.about {
  margin-bottom: 40px;
  color: var(--color-text-dark)
}
</style>